public String autogeneratedID(){
    
    String code = null;
    ResultSet rs = null;
    
    try {
        Conf_Con.ConnectionConn();
        Statement stm = Conf_Con.con.createStatement();
        String aut = "select top 1 Supplier_ID from Game.Supplier order by Supplier_ID desc";
        rs = stm.executeQuery(aut);
        if(rs.next()){
            code = rs.getString(1);
            int value = Integer.parseInt(code.substring(1));
            value++;
            if(value>=100){
            code = "S"+value;
            }
            else if(value>=10){
            code = "S0"+value;
            }
            else{
            code = "S00"+value;
            }
        }
        else {
            code = "S001";
        }  
    } catch (Exception e)  {
        System.out.println(e.getMessage());
    }
    return code;
    
}

private void getData() {
        try {
            su.Suppliers_ID = txsp_id.getText();
            su.Suppliers_Name = txsp_name.getText();
            su.Suppliers_Email = txsp_email.getText();
            
        }
        catch(Exception ex) {
            JOptionPane.showMessageDialog(null,ex.getMessage());
        }
        
    }

private void deleteData() {
        this.getData();
        int flag = this.validasi();
        if(flag == 0) {
            int i = su.doDelete();
            if(i == 0) {
                JOptionPane.showMessageDialog(null,"Data Gagal DiHapus!");
            }
            else {
                JOptionPane.showMessageDialog(null,"Data Berhasil Dihapus!");
            }
        }
    }

 private int validasi() {
        int flag = 0;
        if(txsp_id.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"ID tidak boleh kosong!");
            flag = 1;
        }
        if(txsp_name.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Nama tidak boleh kosong!");
            flag = 1;
        }
	if(txsp_email.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"Email tidak boleh kosong!");
            flag = 1;
            
        }
        return flag;
        
    }
 
 
    private void sp_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sp_addActionPerformed
        
        if(txsp_email.getText().contains("@gmail.com")) {
            warning2.setVisible(true);
        }
        if(txsp_name.getText().equals("")) {
            warning1.setVisible(true);
        }
        if(txsp_email.getText().equals("")) {
            warning2.setVisible(true);
        }

        else{
            String sp_id=txsp_id.getText();
            String sp_name=txsp_name.getText();
            String sp_email=txsp_email.getText()+"@gmail.com";
           
            PreparedStatement ps;
            String query="INSERT INTO Game.Supplier(Supplier_ID,Supplier_Name,Email) values (?,?,?)";
            try {
                ps=MyConnection.getConnection().prepareStatement(query);
            
                ps.setString(1, sp_id);
                ps.setString(2, sp_name);
                ps.setString(3, sp_email);
            
                if(ps.executeUpdate()>0 )
                {
                    JOptionPane.showMessageDialog(null, "New Supplier add!");
                    this.dispose();
                    Supplier sp = new Supplier();
                    sp.show();
                }
            else{
            JOptionPane.showMessageDialog(null, "Data Can't be empty");
            }
              } catch (SQLException ex ) {
            JOptionPane.showMessageDialog(null, "Connection Failed!");
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        
    }//GEN-LAST:event_sp_addActionPerformed

