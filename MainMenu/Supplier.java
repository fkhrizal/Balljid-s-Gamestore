    public ArrayList<User>userList(){
        ArrayList<User>userList=new ArrayList<>();
        try {
            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            String url = "jdbc:sqlserver://localhost:1433;databaseName=GameStore;integratedSecurity=true";
            String Username = "";
            String Password = "";
            Connection con = DriverManager.getConnection(url,Username,Password);
            String query="SELECT * FROM Game.Supplier";
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(query);
            User user;
            while(rs.next()){
                user=new User(rs.getString("Supplier_ID"),rs.getString("Supplier_Name"),rs.getString("Email"));
                userList.add(user);
            }
        } 
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
        }
        return userList;
        }
    
